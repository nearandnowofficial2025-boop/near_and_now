function FAQItem({ q, a }: { q: string; a: string }) {
  const [open, setOpen] = useState(false);
  return (
    <div
      className={`border rounded-xl transition-all duration-200 overflow-hidden cursor-pointer ${open ? 'border-emerald-200 bg-emerald-50/40' : 'border-gray-200 bg-white hover:border-gray-300'}`}
      onClick={() => setOpen(!open)}
    >
      <div className="flex items-center justify-between px-5 py-4">
        <p className={`text-sm font-semibold pr-4 ${open ? 'text-emerald-700' : 'text-gray-800'}`}>{q}</p>
        <ChevronDown
          size={16}
          className={`flex-shrink-0 transition-transform duration-200 ${open ? 'rotate-180 text-emerald-500' : 'text-gray-400'}`}
        />
      </div>
      {open && (
        <div className="px-5 pb-4">
          <div className="w-full h-px bg-emerald-200 mb-3" />
          <p className="text-sm text-gray-600 leading-relaxed">{a}</p>
        </div>
      )}
    </div>
  );
}

